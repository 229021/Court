package com.san.platform.JSON;

import java.io.Serializable;

public class CreateFace implements Serializable {


    // 人脸Url
    private String faceURL;
    // 人脸库类型
    private String faceLibType;
    // 人脸库ID
    private String FDID;
    // 人脸数据ID
    private String FPID;
    // 姓名
    private String name;
    // 性别
    private String gender;
    // 出生日期
    private String bornTime;
    // 城市
    private String city;
    // 证件类型
    private String certificateType;
    // 证件号
    private String certificateNumber;
    // 案件信息
    private String caseInfo;
    // 自定义标签
    private String tag;
    // 人员住址
    private String address;
    // 自定义信息
    private String customInfo;


    public String getFaceURL() {
        return faceURL;
    }

    public void setFaceURL(String faceURL) {
        this.faceURL = faceURL;
    }

    public String getFaceLibType() {
        return faceLibType;
    }

    public void setFaceLibType(String faceLibType) {
        this.faceLibType = faceLibType;
    }

    public String getFDID() {
        return FDID;
    }

    public void setFDID(String FDID) {
        this.FDID = FDID;
    }

    public String getFPID() {
        return FPID;
    }

    public void setFPID(String FPID) {
        this.FPID = FPID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getBornTime() {
        return bornTime;
    }

    public void setBornTime(String bornTime) {
        this.bornTime = bornTime;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getCertificateType() {
        return certificateType;
    }

    public void setCertificateType(String certificateType) {
        this.certificateType = certificateType;
    }

    public String getCertificateNumber() {
        return certificateNumber;
    }

    public void setCertificateNumber(String certificateNumber) {
        this.certificateNumber = certificateNumber;
    }

    public String getCaseInfo() {
        return caseInfo;
    }

    public void setCaseInfo(String caseInfo) {
        this.caseInfo = caseInfo;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCustomInfo() {
        return customInfo;
    }

    public void setCustomInfo(String customInfo) {
        this.customInfo = customInfo;
    }

    @Override
    public String toString() {
        return "CreateFace{" +
                "faceURL='" + faceURL + '\'' +
                ", faceLibType='" + faceLibType + '\'' +
                ", FDID='" + FDID + '\'' +
                ", FPID='" + FPID + '\'' +
                ", name='" + name + '\'' +
                ", gender='" + gender + '\'' +
                ", bornTime='" + bornTime + '\'' +
                ", city='" + city + '\'' +
                ", certificateType='" + certificateType + '\'' +
                ", certificateNumber='" + certificateNumber + '\'' +
                ", caseInfo='" + caseInfo + '\'' +
                ", tag='" + tag + '\'' +
                ", address='" + address + '\'' +
                ", customInfo='" + customInfo + '\'' +
                '}';
    }
}
