package com.san.platform.JSON;

/**
 * 海康- 人脸解析
 */
public class AnalysisFace {

    private String imagesType = "URL";
    private String imagesData = "";
    private String algorithmType = "faceModel";
    private String mode = "mutiface";

    public String getImagesType() {
        return imagesType;
    }

    public void setImagesType(String imagesType) {
        this.imagesType = imagesType;
    }

    public String getImagesData() {
        return imagesData;
    }

    public void setImagesData(String imagesData) {
        this.imagesData = imagesData;
    }

    public String getAlgorithmType() {
        return algorithmType;
    }

    public void setAlgorithmType(String algorithmType) {
        this.algorithmType = algorithmType;
    }

    public String getMode() {
        return mode;
    }

    public void setMode(String mode) {
        this.mode = mode;
    }
}
